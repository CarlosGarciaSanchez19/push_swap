#include "push_swap.h"

char	*ft_strdup(const char *src)
{
	char	*c;
	int		i;

	i = ft_strlen(src);
	c = (char *)malloc(i + 1);
	if (c == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		c[i] = src[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
